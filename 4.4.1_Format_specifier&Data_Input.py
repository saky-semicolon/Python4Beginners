
name = str(input("Enter your name: "))
age = int(input("Enter your age: "))
height = float(input("Enter your height: "))

print("My name is %s and I am %d years old. I am %.1f feet tall." % (name, age, height))

"""
Format Specifier
1. name = "%s" - This is a string format specifier that can be used to store a person's name.

2. age = "%d" - This is an integer format specifier that can be used to store a person's age.

3. height = "%f" - This is a floating-point format specifier that can be used to store a person's height.

"""