#In Python, use the = operator to assign values to variables.

youngerBrother = "S M ASIFUL ISLAM SAKY"
elderBrother = "S M ARIFUL ISLAM SANY"

print(youngerBrother)

#S M ASIFUL ISLAM SAKY

print(elderBrother)

#S M ARIFUL ISLAM SANY

#Assign multiple values to multiple variables

#You can assign multiple values to multiple variables by separating variables and values with commas ,.

firstName, lastName = "S M Asiful Islam", "Saky"

print(firstName)
# Asiful Islam Saky

print(lastName)
# Saky



#Assign the same value to multiple variables

#You can assign the same value to multiple variables by using = consecutively.

a = b = 100

print(a)
# 100

print(b)
# 100


